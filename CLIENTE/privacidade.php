    <h1>POLÍTICA DE PRIVACIDADE</h1>

<p><h2>Este site é mantido e operado por Demadan Store.</h2></p>
Nós coletamos e utilizamos alguns dados pessoais que pertencem aqueles que utilizam nosso site. <br> Ao fazê-lo, agimos na qualidade de controlador desses dados e estamos sujeitos às disposições da Lei Federal n. 13.709/2018 (Lei Geral de Proteção de Dados Pessoais - LGPD).
<br>Nós cuidamos da proteção de seus dados pessoais e, por isso, disponibilizamos esta política de privacidade, que contém informações importantes sobre:
<p> - Quem deve utilizar nosso site </p>
<p> - Quais dados coletamos e o que fazemos com eles; </p>
<p> - Seus direitos em relação aos seus dados pessoais; e </p>
<p> - Como entrar em contato conosco.</p>
<p><h3>Dados que coletamos e motivos da coleta</h3>
Nosso site coleta e utiliza alguns dados pessoais de nossos usuários, de acordo com o disposto nesta seção.</p>
<p><h3>1. Dados pessoais fornecidos expressamente pelo usuário</h3>
Nós coletamos os seguintes dados pessoais que nossos usuários nos fornecem expressamente ao utilizar nosso site:
<ul>
   <li>NOME</li> 
   <li>CPF</li>  
   <li>E-MAIL</li> 
</ul></p>
<p> A coleta destes dados ocorre nos seguintes momentos: <br>
Quando faz cadastro em nosso site.</p> 
<p> Os dados fornecidos por nossos usuários são coletados com as seguintes finalidades: <br>
Para que o usuário possa comprar nossos produtos com segurança.</p>
<p><h3> 2. Dados sensíveis </h3>
<p> Não serão coletados dados sensíveis de nossos usuários, assim entendidos aqueles definidos nos arts. 11 e seguintes da Lei de Proteção de Dados Pessoais. <br> Assim, não haverá coleta de dados sobre origem racial ou étnica, convicção religiosa, opinião política, filiação a sindicato ou a organização de caráter religioso, filosófico ou político, dado referente à saúde ou à vida sexual, dado genético ou biométrico, quando vinculado a uma pessoa natural. </p>
<p>Vale dizer que, se houver alguma justificativa legal ou regulatória, os dados poderão continuar armazenados ainda que a finalidade para a qual foram coletados ou tenham sido tratados tenha se exaurido. <br>
Uma vez finalizado o tratamento, observadas as disposições desta seção, os dados são apagados ou anonimizados.
<p><h3>3. Bases legais para o tratamento de dados pessoais </h3></p>
Cada operação de tratamento de dados pessoais precisa ter um fundamento jurídico, ou seja, uma base legal, que nada mais é que uma justificativa que a autorize, prevista na Lei Geral de Proteção de Dados Pessoais. <br>
Todas as Nossas atividades de tratamento de dados pessoais possuem uma base legal que as fundamenta, dentre as permitidas pela legislação. <br> Mais informações sobre as bases legais que utilizamos para operações de tratamento de dados pessoais específicas podem ser obtidas a partir de nossos canais de contato, informados ao final desta Política
</p>    
<p><h3>4. Como o titular pode exercer seus direitos </h3> <br>
Para garantir que o usuário que pretende exercer seus direitos é, de fato, o titular dos dados pessoais objeto da requisição, poderemos solicitar documentos ou outras informações que possam auxiliar em sua correta identificação, a fim de resguardar nossos direitos e os direitos de terceiros. Isto somente será feito, porém, se for absolutamente necessário, e o requerente receberá todas as informações relacionadas.</p>
<p><h3> 5. Medidas de segurança no tratamento de dados pessoais</h3> <br>
Empregamos medidas técnicas e organizativas aptas a proteger os dados pessoais de acessos não autorizados e de situações de destruição, perda, extravio ou alteração desses dados. <br>
As medidas que utilizamos levam em consideração a natureza dos dados, o contexto e a finalidade do tratamento, os riscos que uma eventual violação geraria para os direitos e liberdades do usuário, e os padrões atualmente empregados no mercado por empresas semelhantes à nossa. <br>
Entre as medidas de segurança adotadas por nós, destacamos as seguintes: <br>
Nosso sistema tem acesso restrito e dados importantes são guardado com hashes para evitar o vazamento de dados e possíveis fraudes.</p>
<p>Ainda que adote tudo o que está ao seu alcance para evitar incidentes de segurança, é possível que ocorra algum problema motivado exclusivamente por um terceiro - como em caso de ataques de hackers ou crackers ou, ainda, em caso de culpa exclusiva do usuário, que ocorre, por exemplo, quando ele mesmo transfere seus dados a terceiro. <br> Assim, embora sejamos, em geral, responsáveis pelos dados pessoais que tratamos, nos eximimos de responsabilidade caso ocorra uma situação excepcional como essas, sobre as quais não temos nenhum tipo de controle. <br>
De qualquer forma, caso ocorra qualquer tipo de incidente de segurança que possa gerar risco ou dano relevante para qualquer de nossos usuários, comunicaremos os afetados e a Autoridade Nacional de Proteção de Dados acerca do ocorrido, em conformidade com o disposto na Lei Geral de Proteção de Dados.
</p>
